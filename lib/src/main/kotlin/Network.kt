/**
 *
 * The fixed key for the main SSB network
 *
 * see https://ssbc.github.io/scuttlebutt-protocol-guide/#handshake for more info
 *
 */
val SSB_MAIN_NETWORK = byteArrayOfInts(0xd4, 0xa1, 0xcb, 0x88, 0xa6, 0x6f, 0x02, 0xf8, 0xdb, 0x63, 0x5c, 0xe2, 0x64, 0x41, 0xcc, 0x5d,
                                       0xac, 0x1b, 0x08, 0x42, 0x0c, 0xea, 0xac, 0x23, 0x08, 0x39, 0xb7, 0x55, 0x84, 0x5a, 0x9f, 0xfb)

private fun byteArrayOfInts(vararg ints: Int) = ByteArray(ints.size) { pos ->
    ints[pos].toByte()
}

